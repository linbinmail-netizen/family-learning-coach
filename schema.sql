Run `schema.sql` in Supabase SQL Editor to create the first version of the database.

The first schema includes:

- MIA and EVA student profiles
- Grade 8 and Grade 9 subject paths
- Starter TEKS-aligned knowledge points
- Diagnostic session records
- Mastery results
- Learning activity records

The current policies are temporary for MVP testing. Before public launch, replace them with authenticated parent/student access rules.
